/** id から路線を取得。未知/未指定はノーマル。 */
export function getLine(id) {
  return LINES[id] && LINES[id].id ? LINES[id] : LINES.normal;
}

/** マス n の駅ナンバー文字列(例: 'JY08')。番号の無い駅は null。 */
export function stationCode(lineObj, n) {
  if (!lineObj.code || n < 1 || n > lineObj.coded) return null;
  return lineObj.code + String(n).padStart(2, '0');
}

/** マス n の駅名(漢字)。駅が無いマスは null。 */
export function stationName(lineObj, n) {
  return lineObj.stations[n - 1]?.[0] ?? null;
}

/** マス n の駅名の読み(ふりがな)。駅が無いマスは null。 */
export function stationYomi(lineObj, n) {
  return lineObj.stations[n - 1]?.[1] ?? null;
}
